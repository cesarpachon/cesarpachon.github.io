<h3>Descripción</h3>
<p>
El concepto de Software Libre ó Open Source siempre me ha llamado la atención. Ha formado parte de mi vida profesional como desarrollador de software y como usuario final.
</p>
<p>
En un contexto social en donde la piratería de software se considera una práctica aceptable, especialmente en pequeñas organizaciones, es importante llamar la atención sobre los riesgos de esta práctica: la instalación de software pirata es la forma principal de ingreso de virus y malware a los computadores de una empresa (seguida por la navegación en internet por sitios dudosos, buscando cracks y seriales). </p>

<p>Este riesgo no es justificable ya que si bien hay algunos casos en los que el software comercial es superior (particularmente en el àrea de diseño gráfico) al considerar el uso que hacen las empresas del mismo, es claro que sólo usan un subconjunto de dicha funcionalidad que es ampliamente soportado por alternativas libres.  
</p>

